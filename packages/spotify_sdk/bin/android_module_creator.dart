import 'dart:io';

import 'package:logger/logger.dart';

import 'android_setup.dart';

/// Responsible for creating a new android module in the /android directory
/// of the flutter project.
class AndroidModuleCreator {
  AndroidModuleCreator(this.moduleName, this.aarFileName, {this.basePath = ''});
  String moduleName;
  String aarFileName;
  String basePath;

  String get _prefix => basePath.isEmpty
      ? ''
      : basePath.endsWith('/')
      ? basePath
      : '$basePath/';

  String _gradleFileContent(String aarFileName) =>
      '''
configurations.maybeCreate("default")
artifacts.add("default", file('$aarFileName'))
''';

  String _readmeContent(String moduleName, String aarFileName) =>
      '''
# Spotify Android SDK for Flutter
This module (`:$moduleName`) was generated by running the `android_setup` script of
the [spotify_sdk](https://pub.dev/packages/spotify_sdk) plugin.

If you want to update the `aar`-library, please run the `android_setup` script using
one of the following commands:
```
dart run spotify_sdk:android_setup --cleanup
flutter pub run spotify_sdk:android_setup --cleanup
```
''';

  Future<void> createModuleDirectory() async {
    // create directory in /android
    final aarDir = await Directory('${_prefix}android/$moduleName').create();
    logger.t('created new directory ${aarDir.path}');

    // create build.gradle file
    final gradleFile = await File('${aarDir.path}/build.gradle').create();
    logger.t('created new file ${gradleFile.path}');
    await gradleFile.writeAsString(_gradleFileContent(aarFileName));

    await _settingsGradle(logger);

    await _appAndroidManifest(logger);

    // create README.md file
    final readmeFile = await File('${aarDir.path}/README.md').create();
    logger.t('created new file ${readmeFile.path}');
    await readmeFile.writeAsString(_readmeContent(moduleName, aarFileName));
  }

  Future<void> _settingsGradle(Logger logger) async {
    // Check which settings file exists
    final settingsFile =
        File('${_prefix}android/settings.gradle.kts').existsSync()
        ? File('${_prefix}android/settings.gradle.kts')
        : File('${_prefix}android/settings.gradle');

    if (!settingsFile.existsSync()) {
      logger.e(
        'Error: Neither settings.gradle.kts nor settings.gradle found in ${_prefix}android/.',
      );
      return;
    }

    final content = await settingsFile.readAsString();
    final isKotlinDsl = settingsFile.path.endsWith('.kts');
    final includeStatement = isKotlinDsl
        ? 'include(":$moduleName")'
        : "include ':$moduleName'";

    // Check both Groovy and Kotlin styles
    if (content.contains('include(":$moduleName")') ||
        content.contains("include(':$moduleName')") ||
        content.contains('include ":$moduleName"') ||
        content.contains("include ':$moduleName'")) {
      logger.t(
        '${settingsFile.path} already contains include statement '
        'for $moduleName',
      );
      return;
    }

    final String newContent;
    if (isKotlinDsl) {
      if (content.contains('include(":app")')) {
        newContent = content.replaceFirst(
          'include(":app")',
          'include(":app")\ninclude(":$moduleName")',
        );
      } else if (content.contains("include(':app')")) {
        newContent = content.replaceFirst(
          "include(':app')",
          "include(':app')\ninclude(':$moduleName')",
        );
      } else {
        newContent = '${content.trim()}\ninclude(":$moduleName")\n';
      }
    } else {
      if (content.contains("include ':app'")) {
        newContent = content.replaceFirst(
          "include ':app'",
          "include ':app'\ninclude ':$moduleName'",
        );
      } else if (content.contains('include ":app"')) {
        newContent = content.replaceFirst(
          'include ":app"',
          'include ":app"\ninclude ":$moduleName"',
        );
      } else {
        newContent = '${content.trim()}\ninclude ":$moduleName"\n';
      }
    }
    await settingsFile.writeAsString(newContent);
    logger.t('added "$includeStatement" to ${settingsFile.path}');
  }

  Future<void> _appAndroidManifest(Logger logger) async {
    // edit app/src/main/AndroidManifest.xml file
    final manifestPath = '${_prefix}android/app/src/main/AndroidManifest.xml';
    final manifestFile = File(manifestPath);
    if (!manifestFile.existsSync()) {
      logger.t(
        'AndroidManifest.xml does not exist at ${manifestFile.path}. '
        'Skipping manifest injection.',
      );
      return;
    }
    final content = await manifestFile.readAsString();
    const activityName =
        'com.spotify.sdk.android.auth.RedirectUriReceiverActivity';
    if (content.contains(activityName)) {
      logger.t(
        'AndroidManifest.xml already contains RedirectUriReceiverActivity.',
      );
      return;
    }

    if (!content.contains('</application>')) {
      logger.e(
        'Error: AndroidManifest.xml does not contain </application> tag.',
      );
      return;
    }

    const activityBlock = '''
        <!-- Added by spotify_sdk setup -->
        <activity
            android:name="com.spotify.sdk.android.auth.RedirectUriReceiverActivity"
            android:exported="true">
            <intent-filter>
                <action android:name="android.intent.action.VIEW"/>
                <category android:name="android.intent.category.DEFAULT"/>
                <category android:name="android.intent.category.BROWSABLE"/>
                <data
                    android:scheme="spotify-sdk"
                    android:host="auth"/>
            </intent-filter>
        </activity>
    </application>''';

    final newContent = content.replaceFirst('</application>', activityBlock);
    await manifestFile.writeAsString(newContent);
    logger.t('added RedirectUriReceiverActivity to AndroidManifest.xml');
  }
}
